function getTile(currentChar) {
	var tile = "<div class='tile'>\n";
	for(var i = 0; i < tileRows; i++){
		for(var j = 0; j < tileCols; j++){
			tile += "<div class='pixelval"+currentChar[i][j]+"' id='row"+i+"col"+j+"'></div>\n"
		}
	}
	tile += "</div>\n";
	return tile;
}

function printBoard(msg) {
	var board = "";
	var msg = msg.replace(/\\n/g, "\n");
	var tileIndex = 0;
	var msgIndex = 0;
	while(tileIndex < boardRows * boardCols){
		var currentChar;
		if(msgIndex < msg.length && msg[msgIndex] == "\n") {
			var start = tileIndex % boardCols;
			while(start > 0 && start < boardCols) {
				currentChar = map["SPC"];
				board += getTile(currentChar);
				tileIndex++;
				start++;
			}
			msgIndex++;
		}

		else {
			if(msgIndex < msg.length && map.hasOwnProperty(msg[msgIndex])){
				currentChar = map[msg[msgIndex]];
			}
			else {
				currentChar = map["SPC"];
			}
			board += getTile(currentChar);
			tileIndex++;
			msgIndex++;
		}
	}
	document.getElementById("board").innerHTML = board;
}

function clearBoard() {
	document.getElementById("text").value = "";
	var board = "";
	var i = 0;
	while(i < boardRows * boardCols){
		board += getTile(map["SPC"]);
		i++;
	}
	document.getElementById("board").innerHTML = board;
}

window.addEventListener("load", function () {
	clearBoard();
});

document.getElementById("submitbtn").addEventListener("click", function () {
	var msg = document.getElementById("text").value;
	printBoard(msg.toUpperCase());
});

document.getElementById("clearbtn").addEventListener("click", function () {
	clearBoard();
});

document.getElementById("text").onkeydown = function(e) {
	if(e.keyCode === 27){
		clearBoard();
	}
	else if(e.keyCode === 13) {
		document.getElementById("submitbtn").click();
	}
};

